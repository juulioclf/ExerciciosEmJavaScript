/* 3: Crie uma função que recebe dois parâmetros, base e expoente, e retorne a base elevada ao expoente.
*/

const powerUp = (base, exponent) => {
    return Math.pow(base, exponent)
}

console.log(powerUp(2,2)) //4
console.log(powerUp(2,5)) //32
console.log(powerUp(4,2)) //16
console.log(powerUp(4,3)) //64